#ifndef LIB_ERROR_H_
#define LIB_ERROR_H_

#ifndef ENONE
#define ENONE        0  /* No any error */
#endif

#ifndef E2BIG
#define E2BIG        1  /* Argument list too long */
#endif

#ifndef ENOMEM
#define ENOMEM       2  /* Out of memory */
#endif

#ifndef ENXIO
#define ENXIO        3  /* No such device or address */
#endif

#ifndef EFAULT
#define EFAULT       4  /* Bad address */
#endif

#ifndef EBUSY
#define EBUSY        5  /* Device or resource busy */
#endif

#ifndef EEXIST
#define EEXIST       6  /* File exists */
#endif

#ifndef ENODEV
#define ENODEV       7  /* No such device */
#endif

#ifndef EINVAL
#define EINVAL       8  /* Invalid argument */
#endif

#ifndef ENOSPC
#define ENOSPC       9  /* No space left on device */
#endif

#ifndef ESTATE
#define ESTATE      10  /* error state */
#endif

#ifndef ENULL
#define ENULL       11  /* NULL Point */
#endif

#ifndef ECOMMU
#define ECOMMU      12  /* Communication error */
#endif

#ifndef ECRC
#define ECRC        13  /* Crc verification error */
#endif

#ifndef EFEB
#define EFEB        14  /* FeedBack err */
#endif

#ifndef EETH
#define EETH        15  /* Exceed Threshold */
#endif

#ifndef ECHILD
#define ECHILD      16  /* No child processes */
#endif

#endif
